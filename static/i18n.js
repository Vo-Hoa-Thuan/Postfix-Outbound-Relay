let currentLang = localStorage.getItem('rp_lang') || 'vn';

function applyTranslations() {
    const langData = translations[currentLang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        
        // Handle attribute translation if the key starts with [attr]
        if (key.startsWith('[placeholder]')) {
            const realKey = key.replace('[placeholder]', '');
            if (langData[realKey]) el.placeholder = langData[realKey];
            return;
        }
        if (key.startsWith('[value]')) {
            const realKey = key.replace('[value]', '');
            if (langData[realKey]) el.value = langData[realKey];
            return;
        }
        if (key.startsWith('[title]')) {
            const realKey = key.replace('[title]', '');
            if (langData[realKey]) el.title = langData[realKey];
            return;
        }

        if (langData[key]) {
            el.textContent = langData[key];
        }
    });

    // Handle special cases: language specific elements
    document.documentElement.lang = currentLang;
}

function switchLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('rp_lang', lang);
        applyTranslations();
        window.dispatchEvent(new CustomEvent('langChanged', { detail: lang }));
    }
}

function getI18nStatus(status) {
    if (!status) return "";
    const key = 'status_' + status.toLowerCase();
    return (translations[currentLang] && translations[currentLang][key]) ? translations[currentLang][key] : status;
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
